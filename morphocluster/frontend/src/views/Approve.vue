<template>
    <div id="approve" class="d-flex flex-column fill-height">
        <div v-if="loading">Loading...</div>
        <div id="node-info">
            <div class="info-hint mdi mdi-information-outline" v-tooltip.hover.html
                title="All members of this node, most extreme appearance first." />
            <!--<node-header :node="node" v-if="node" />-->

            <div class="row flex-1 overflow-y-auto" v-if="node_members">
                <div v-for="m of node_members" :key="getUniqueId(m)" class="col col-2">
                    <member-preview :member="m" :controls="member_controls" v-on:moveup="moveupMember" />
                </div>
            </div>
            <infinite-loading v-if="node" @infinite="updateMembers" spinner="circles">
                <div slot="no-more" />
            </infinite-loading>
        </div>
        <div id="progress" v-if="progress" v-tooltip.hover
            :title="`${progress.leaves_n_approved_objects.toLocaleString('en-US')} / ${progress.leaves_n_objects.toLocaleString('en-US')}`">
            <div id="progress-bar-wrapper">
                <div :style="{ width: (progress.leaves_n_approved_objects / progress.leaves_n_objects * 100) + '%' }"
                    class="progress-bar"></div>
            </div>
        </div>
        <div id="decision">
            <v-tooltip bottom>
                <template v-slot:activator="{ on }">
                    <v-btn id="btn-approve" color="success" @click.prevent="approve(true)" v-on="on">
                        <i class="mdi mdi-check-all" /><i class="mdi mdi-flag" />
                        Approve + Flag
                    </v-btn>
                </template>
                All members look alike and this cluster is exceptional. Approve and flag for preferred
                treatment.<kbd>F</kbd>
            </v-tooltip>
            <v-tooltip bottom>
                <template v-slot:activator="{ on }">
                    <v-btn id="btn-approve" color="success" @click.prevent="approve(false)" v-on="on">
                        <i class="mdi mdi-check-all" /> Approve
                    </v-btn>
                </template>
                All members look alike. Approve .<kbd>A</kbd>
            </v-tooltip>
            <v-tooltip bottom>
                <template v-slot:activator="{ on }">
                    <v-btn id="btn-merge" color="error" @click.prevent="merge" v-on="on">
                        <i class="mdi mdi-call-merge" /> Merge into parent
                    </v-btn>
                </template>
                Members are too dissimilar. Merge into parent. <kbd>M</kbd>
            </v-tooltip>
        </div>
        <message-log class="bg-light" :messages="messages" />
        <v-dialog v-model="doneModal" centered no-title no-fade ref="doneModal">
            <v-card>
                <v-card-title class="success">
                    Approval done
                </v-card-title>
                <v-card-text>
                    <div class="text-center">
                        Approval is done for this project.
                    </div>
                </v-card-text>
                <v-card-actions>
                    <v-btn @click="$router.push({ name: 'projects' })" color="primary">
                        Back to projects
                    </v-btn>
                </v-card-actions>
            </v-card>
        </v-dialog>
    </div>
</template>

<script>
import axios from "axios";

import InfiniteLoading from "vue-infinite-loading";

import mixins from "@/mixins.js";

import * as api from "@/helpers/api.js";

import MemberPreview from "@/components/MemberPreview.vue";
import MessageLog from "@/components/MessageLog.vue";

export default {
    name: "ApproveView",
    data() {
        return {
            doneModal: false,
            loading: true,
            project: null,
            node: null,
            node_members: [],
            members_url: null,
            progress: null,
            member_controls: [
                {
                    event: "moveup",
                    icon: "mdi-arrow-up",
                    title: "Move this member to the parent node.",
                },
            ],
        };
    },
    created() {
        this.initialize();
    },
    mounted() {
        window.addEventListener("keypress", this.keypress);
    },
    beforeDestroy() {
        window.removeEventListener("keypress", this.keypress);
    },
    components: {
        MemberPreview,
        MessageLog,
        InfiniteLoading,
    },
    mixins: [mixins],
    watch: {
        $route: "initialize",
    },
    methods: {
        initialize() {
            this.loading = true;
            this.node = null;
            this.node_members = [];
            this.members_url = null;

            const project_id = parseInt(this.$route.params.project_id);

            var projectLoaded = new Promise((resolve) => {
                if (this.project && this.project.project_id == project_id) {
                    console.log("Project was already loaded.");
                    resolve();
                } else {
                    console.log("Loading project.");
                    this.project = null;
                    this.progress = null;
                    api.getProject(project_id, true).then((project) => {
                        this.project = project;
                        console.log(project);
                        this.progress = project.progress;
                        resolve();
                    });
                }
            });

            projectLoaded
                .then(() => {
                    // If we already have a node_id, return it
                    if (this.$route.params.node_id) {
                        return parseInt(this.$route.params.node_id);
                    }
                    // ... otherwise get the next node
                    return api
                        .getNextUnapprovedNode(this.project.node_id, {
                            leaf: true,
                        })
                        .then((node_id) => {
                            if (node_id == null) {
                                if (this.$refs.doneModal) {
                                    this.$refs.doneModal.show();
                                };
                                return null;
                            }
                            const to = {
                                name: "approve",
                                params: {
                                    project_id: project_id,
                                    node_id: node_id,
                                },
                            };

                            this.$router.replace(to);
                            return node_id;
                        });
                })
                .then((node_id) => {
                    if (node_id == null) {
                        return;
                    }
                    // Avoid double-load by $router.replace(to) -trigger-> initialize
                    if (this.node && this.node.node_id == node_id) {
                        return;
                    }
                    console.log(`Loading node ${node_id}...`);
                    return api.getNode(node_id).then((node) => {
                        this.node = node;
                    });
                })
                .then(() => {
                    this.loading = false;
                })
                .catch((e) => {
                    this.axiosErrorHandler(e);
                });
        },
        // updateMembers gets called as an infinite loading handler.
        updateMembers($state) {
            // Should members_url be updated (with unique id etc.) on response?
            var updateMembersUrl = false;

            if (!this.members_url) {
                const nodes = this.node.children;
                this.members_url = `/api/nodes/${this.node.node_id
                    }/members?objects=${!nodes}&nodes=${nodes}&arrange_by=interleaved&`;
                this.page = 0;
                updateMembersUrl = true;
            }

            var url = `${this.members_url}&page=${this.page}`;

            console.log(`Loading ${url}...`);

            axios
                .get(`${this.members_url}&page=${this.page}`)
                .then((response) => {
                    this.node_members = this.node_members.concat(
                        response.data.data
                    );

                    if (updateMembersUrl) {
                        this.members_url = response.data.links.self;
                    }

                    $state.loaded();

                    if (this.page < response.data.meta.last_page) {
                        this.page += 1;
                    } else {
                        $state.complete();
                    }
                })
                .catch((e) => {
                    this.axiosErrorHandler(e);
                });
        },
        approve(preferred = false) {
            console.log("Approve");
            api.patchNode(this.node.node_id, {
                approved: true,
                preferred: preferred,
            })
                .then(() => {
                    const msg = `Approved ${this.node.node_id}.`;
                    console.log(msg);
                    this.messages.unshift(msg);
                    // Update progress
                    api.getNodeProgress(this.project.node_id, {
                        log: "approve",
                    })
                        .then((progress) => {
                            this.progress = progress;
                        })
                        .catch((e) => {
                            this.axiosErrorHandler(e);
                        });

                    const to = {
                        name: "approve",
                        params: {
                            project_id: this.project.project_id,
                        },
                    };

                    this.$router.push(to);
                })
                .catch((e) => {
                    this.axiosErrorHandler(e);
                });
        },
        merge() {
            // TODO
            api.mergeNodeInto(this.node.node_id, this.node.parent_id)
                .then(() => {
                    this.messages.unshift(`Merged ${this.node.node_id}.`);

                    // Update progress
                    api.getNodeProgress(this.project.node_id, {
                        log: "approve",
                    })
                        .then((progress) => {
                            this.progress = progress;
                        })
                        .catch((e) => {
                            this.axiosErrorHandler(e);
                        });

                    const to = {
                        name: "approve",
                        params: {
                            project_id: this.project.project_id,
                        },
                    };

                    this.$router.push(to);
                })
                .catch((e) => {
                    this.axiosErrorHandler(e);
                });
        },
        keypress(event) {
            if (
                this.loading ||
                event.altKey ||
                event.ctrlKey ||
                event.metaKey ||
                event.shiftKey
            ) {
                return;
            }
            if (event.key == "a") {
                this.approve();
            } else if (event.key == "f") {
                this.approve(true);
            } else if (event.key == "m") {
                this.merge();
            }
        },
        moveupMember(member) {
            console.log("Remove", this.getUniqueId(member));

            // TODO: Also reject members.
            api.nodeAdoptMembers(this.node.parent_id, [member])
                .then(() => {
                    // Remove from current recommendations
                    var index = this.node_members.indexOf(member);
                    if (index > -1) {
                        this.node_members.splice(index, 1);
                    }
                })
                .catch((e) => {
                    this.axiosErrorHandler(e);
                });
        },
    },
};
</script>

